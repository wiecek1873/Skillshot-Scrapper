class JobOffer:
    def __init__(
        self,
        title,
        company,
        location,
        remote,
        category,
        job_type,
        seniority,
        technology,
        years_of_experience,
        salary,
        date,
        views,
    ):
        self.title = title
        self.company = company
        self.location = location
        self.remote = remote
        self.category = category
        self.job_type = job_type
        self.seniority = seniority
        self.technology = technology
        self.years_of_experience = years_of_experience
        self.salary = salary
        self.date = date
        self.views = views
