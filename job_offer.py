class JobOffer:
    def __init__(
        self,
        Title,
        Company,
        Location,
        Remote,
        Category,
        Job_Type,
        Seniority,
        Salary,
        Date,
        Views,
        Url
    ):
        self.Title = Title
        self.Company = Company
        self.Location = Location
        self.Remote = Remote
        self.Category = Category
        self.Job_Type = Job_Type
        self.Seniority = Seniority
        self.Salary = Salary
        self.Date = Date
        self.Views = Views
        self.Url = Url