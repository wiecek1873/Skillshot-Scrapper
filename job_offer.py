class JobOffer:
    def __init__(
        self,
        Title,
        Company,
        Location,
        Category,
        Job_Type,
        Seniority,
        Salary_Min,
        Salary_Max,
        Date,
        Views,
        Url
    ):
        self.Title = Title
        self.Company = Company
        self.Location = Location
        self.Category = Category
        self.Job_Type = Job_Type
        self.Seniority = Seniority
        self.Salary_Min = Salary_Min
        self.Salary_Max = Salary_Max
        self.Date = Date
        self.Views = Views
        self.Url = Url